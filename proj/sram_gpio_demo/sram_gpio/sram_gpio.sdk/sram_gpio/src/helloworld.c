/*
 * helloworld.c: simple test application
 *
 * This application configures UART 16550 to baud rate 9600.
 * PS7 UART (Zynq) is not initialized by this application, since
 * bootrom/bsp configures it to baud rate 115200
 *
 * ------------------------------------------------
 * | UART TYPE   BAUD RATE                        |
 * ------------------------------------------------
 *   uartns550   9600
 *   uartlite    Configurable only in HW design
 *   ps7_uart    115200 (configured by bootrom/bsp)
 */

#include <stdio.h>
#include "platform.h"
#include "xparameters.h"
#include "xgpio.h"

/*
 * The following constants map to the XPAR parameters created in the
 * xparameters.h file. They are defined here such that a user can easily
 * change all the needed parameters in one place.
 */
#define GPIO_EXAMPLE_DEVICE_ID  XPAR_GPIO_0_DEVICE_ID

/*
 * The following constant is used to wait after an LED is turned on to make
 * sure that it is visible to the human eye.  This constant might need to be
 * tuned for faster or slower processor speeds.
 */
#define LED_DELAY     1000000

/*
 * The following constant is used to determine which channel of the GPIO is
 * used for the LED if there are 2 channels supported.
 */
#define LED_CHANNEL 1

/**************************** Type Definitions *******************************/



//------------------------jcyuan define
#define direction 0x30
//5 input 4 input 3-0 output








XGpio Gpio; /* The Instance of the GPIO Driver */


int main()
{
    init_platform();

	volatile u32 Data;
	int Status;
	volatile int Delay;

    print("Hello World\n\r");


	/* Initialize the GPIO driver */
	Status = XGpio_Initialize(&Gpio, GPIO_EXAMPLE_DEVICE_ID);
	if (Status != XST_SUCCESS) {
		return XST_FAILURE;
	}

	/* Set the direction for all signals as inputs except the LED output */
	XGpio_SetDataDirection(&Gpio, LED_CHANNEL,direction );

	while(1)
	{
		Data = XGpio_DiscreteRead(&Gpio, LED_CHANNEL);
		if(((Data&0x3f)>>4)==0x3)
		{
			XGpio_DiscreteWrite(&Gpio, LED_CHANNEL, 0x9);
		}
		else
		{
			XGpio_DiscreteWrite(&Gpio, LED_CHANNEL, 0x6);
		}
	}



    cleanup_platform();
    return 0;
}
